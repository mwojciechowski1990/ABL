package com.ul.webserver;

public class Command {
	private String mName;
	private String mValue;
	
	public String getName() {
		return mName;
	}
	public void setName(String mName) {
		this.mName = mName;
	}
	public String getValue() {
		return mValue;
	}
	public void setValue(String mValue) {
		this.mValue = mValue;
	}
	
	
	

}
